let currentLang = localStorage.getItem('wm-lang') || 'en';

/* ══════════════════════════════════
   MOTEUR I18N
══════════════════════════════════ */
function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('wm-lang', lang);

  const isRTL = lang === 'ar';
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  const placeholders = {
    fr: { name: 'Jean Dupont', email: 'jean@email.com', msg: 'Décrivez votre projet...', submit: 'Envoyer →' },
    en: { name: 'John Smith', email: 'john@email.com', msg: 'Describe your project...', submit: 'Send →' },
    ar: { name: 'محمد علي', email: 'example@email.com', msg: 'صف مشروعك...', submit: 'إرسال ←' },
  };
  const p = placeholders[lang];
  const nameInput  = document.getElementById('name');
  const emailInput = document.getElementById('email');
  const msgInput   = document.getElementById('message');
  const submitBtn  = document.querySelector('#contact-form button[type=submit]');
  if (nameInput)  nameInput.placeholder  = p.name;
  if (emailInput) emailInput.placeholder = p.email;
  if (msgInput)   msgInput.placeholder   = p.msg;
  if (submitBtn)  submitBtn.textContent  = p.submit;

  const labels = {
    fr: { name: 'Nom', email: 'Email', project: 'Type de projet', msg: 'Message' },
    en: { name: 'Name', email: 'Email', project: 'Project type', msg: 'Message' },
    ar: { name: 'الاسم', email: 'البريد الإلكتروني', project: 'نوع المشروع', msg: 'الرسالة' },
  };
  const l = labels[lang];
  const labelFor = (id) => document.querySelector(`label[for="${id}"]`);
  if (labelFor('name'))    labelFor('name').textContent    = l.name;
  if (labelFor('email'))   labelFor('email').textContent   = l.email;
  if (labelFor('project')) labelFor('project').textContent = l.project;
  if (labelFor('message')) labelFor('message').textContent = l.msg;

  const selectOpts = {
    fr: ['Choisir...', 'Landing page', 'Site vitrine', 'Maintenance', 'Autre'],
    en: ['Choose...', 'Landing page', 'Showcase website', 'Maintenance', 'Other'],
    ar: ['اختر...', 'صفحة هبوط', 'موقع عرض', 'صيانة', 'أخرى'],
  };
  const select = document.getElementById('project');
  if (select) {
    Array.from(select.options).forEach((opt, i) => {
      if (selectOpts[lang][i]) opt.textContent = selectOpts[lang][i];
    });
  }
}

