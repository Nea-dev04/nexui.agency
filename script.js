const CURRENCY_SYMBOLS = {
  EUR:'€', USD:'$', GBP:'£', AUD:'A$', NZD:'NZ$', CHF:'CHF', CAD:'CA$'
};

const CURRENCY_NAMES_FR = {
  EUR:'Euro', USD:'Dollar américain', GBP:'Livre sterling',
  AUD:'Dollar australien', NZD:'Dollar néo-zélandais', CHF:'Franc suisse'
};

let detectedCurrency = 'EUR';
let exchangeRates = {};

async function detectAndSetCurrency() {
  try {
    // 1. Détecter le pays via IP
    // Essayer plusieurs APIs de géolocalisation
    let countryCode = 'US'; // fallback dollar pour EN
    try {
      const geoRes = await fetch('https://ipapi.co/json/');
      const geo = await geoRes.json();
      countryCode = geo.country_code || 'US';
    } catch {
      try {
        const geoRes2 = await fetch('https://ipwho.is/');
        const geo2 = await geoRes2.json();
        countryCode = geo2.country_code || 'US';
      } catch { countryCode = 'US'; }
    }
    detectedCurrency = COUNTRY_CURRENCY[countryCode] || 'EUR';

    // 2. Récupérer les taux de change (API gratuite)
    if (detectedCurrency !== 'EUR') {
      const rateRes = await fetch(`https://api.frankfurter.app/latest?from=EUR&to=${detectedCurrency}`);
      const rateData = await rateRes.json();
      exchangeRates = rateData.rates || {};
    }

    // 3. Mettre à jour les prix selon la langue active
    updatePricesDisplay();

  } catch (e) {
    // Silencieux — on garde EUR par défaut
  }
}

function updatePricesDisplay() {
  const lang = currentLang;
  const isAR = lang === 'ar';
  const note = document.getElementById('currency-note');

  // En arabe : toujours afficher en $, avec mention
  if (isAR) {
    const rate = exchangeRates['USD'] || 1.08; // fallback si pas chargé
    displayPrices('USD', rate);
    if (note) {
      note.style.display = 'block';
      note.textContent = `* الأسعار تقريبية بالدولار الأمريكي — قد تختلف حسب بلدك`;
    }
    return;
  }

  // En FR : toujours €
  if (lang === 'fr') {
    displayPrices('EUR', 1);
    if (note) note.style.display = 'none';
    return;
  }

  // En EN : devise selon pays ($ par défaut si non détecté)
  if (detectedCurrency === 'EUR' && !exchangeRates['USD']) {
    // Pas encore de détection — afficher $ par défaut pour EN
    displayPrices('USD', 1.08);
    if (note) {
      note.style.display = 'block';
      note.textContent = '* Prices shown in USD — converted from EUR';
    }
  } else {
    const rate = exchangeRates[detectedCurrency] || 1;
    displayPrices(detectedCurrency, rate);
    if (note) {
      const sym = CURRENCY_SYMBOLS[detectedCurrency] || detectedCurrency;
      note.style.display = 'block';
      note.textContent = `* Prices shown in ${sym} (${detectedCurrency}) — converted from EUR`;
    }
  }
}

function displayPrices(currency, rate) {
  const sym = CURRENCY_SYMBOLS[currency] || currency;
  const prices = [
    { id: 'price-1', cur: 'currency-1', base: BASE_PRICES_EUR.p1, monthly: false },
    { id: 'price-2', cur: 'currency-2', base: BASE_PRICES_EUR.p2, monthly: false },
    { id: 'price-3', cur: 'currency-3', base: BASE_PRICES_EUR.p3, monthly: true  },
  ];

  prices.forEach(({ id, cur, base, monthly }) => {
    const priceEl = document.getElementById(id);
    const curEl = document.getElementById(cur);
    if (!priceEl || !curEl) return;

    const converted = Math.round(base * rate);
    priceEl.textContent = converted;

    if (monthly) {
      const monthLabel = currentLang === 'ar' ? 'شهر' : (currentLang === 'fr' ? 'mois' : 'mo');
      curEl.textContent = `${sym}/${monthLabel}`;
    } else {
      curEl.textContent = sym;
    }
  });
}
